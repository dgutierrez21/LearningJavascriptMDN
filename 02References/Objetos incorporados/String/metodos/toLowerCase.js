// String.prototype.toLowerCase()
// El método toLowerCase() devuelve el valor de cadena de llamada convertido a minúsculas.

// Sintaxis
// toLowerCase()

// Valor devuelto
// Una nueva cadena que representa la cadena de llamada convertida a minúsculas.

// Descripción
// El método toLowerCase() devuelve el valor de la cadena convertida a minúsculas. toLowerCase() no afecta al valor de la cadena str en sí.

// Ejemplos;

console.log(string.toLowerCase());

console.log("EL ZORRO MARRÓN".toLowerCase());
